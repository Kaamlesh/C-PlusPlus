#include <iostream>
using namespace std;
int main() 
{
    int a = 10, b = 3;
    cout << "ARITHMETIC OPERATORS IN C++" << endl;
    cout << "----------------------------" << endl;
    cout << "a = " << a << ", b = " << b << endl;
    cout << "Addition (a + b)       = " << a + b << endl;
    cout << "Subtraction (a - b)    = " << a - b << endl;
    cout << "Multiplication (a * b) = " << a * b << endl;
    cout << "Division (a / b)       = " << a / b << endl;
    cout << "Modulus (a % b)        = " << a % b << endl;
    return 0;
}