#include <iostream>
using namespace std;
int main() 
{
    int a = 10, b = 20;
    cout << "RELATIONAL OPERATORS IN C++" << endl;
    cout << "---------------------------" << endl;
    cout << "a = " << a << ", b = " << b << endl;
    cout << "a < b  = " << (a < b) << endl;
    cout << "a <= b = " << (a <= b) << endl;
    cout << "a > b  = " << (a > b) << endl;
    cout << "a >= b = " << (a >= b) << endl;
    cout << "a == b = " << (a == b) << endl;
    cout << "a != b = " << (a != b) << endl;
    return 0;
}